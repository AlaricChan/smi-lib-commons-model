package com.dell.isg.smi.commons.model.common;

/**
 * The Class CallBackResponse.
 *
 * @author rahman.muhammad
 */
public class CallBackResponse {
    private String id;
    private String name;
    private String descrioption;
    private String data;


    /**
     * Gets the id.
     *
     * @return the id
     */
    public String getId() {
        return id;
    }


    /**
     * Sets the id.
     *
     * @param id the new id
     */
    public void setId(String id) {
        this.id = id;
    }


    /**
     * Gets the name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }


    /**
     * Sets the name.
     *
     * @param name the new name
     */
    public void setName(String name) {
        this.name = name;
    }


    /**
     * Gets the descrioption.
     *
     * @return the descrioption
     */
    public String getDescrioption() {
        return descrioption;
    }


    /**
     * Sets the descrioption.
     *
     * @param descrioption the new descrioption
     */
    public void setDescrioption(String descrioption) {
        this.descrioption = descrioption;
    }


    /**
     * Gets the data.
     *
     * @return the data
     */
    public String getData() {
        return data;
    }


    /**
     * Sets the data.
     *
     * @param data the new data
     */
    public void setData(String data) {
        this.data = data;
    }

}
